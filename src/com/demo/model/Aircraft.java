package com.demo.model;

public class Aircraft {
   private int id;
   private String name;
   private double cost;
   private int maxSeat;
   private int range;
   private int speed;
   private int fuelCapacity;
   private int fuelBurn;
   private int takingOffDis;
   private int landingDis;
   
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public double getCost() {
	return cost;
}
public void setCost(double cost) {
	this.cost = cost;
}
public int getMaxSeat() {
	return maxSeat;
}
public void setMaxSeat(int maxSeat) {
	this.maxSeat = maxSeat;
}
public int getRange() {
	return range;
}
public void setRange(int range) {
	this.range = range;
}
public int getSpeed() {
	return speed;
}
public void setSpeed(int speed) {
	this.speed = speed;
}
public int getFuelCapacity() {
	return fuelCapacity;
}
public void setFuelCapacity(int fuelCapacity) {
	this.fuelCapacity = fuelCapacity;
}
public int getFuelBurn() {
	return fuelBurn;
}
public void setFuelBurn(int fuelBurn) {
	this.fuelBurn = fuelBurn;
}
public int getTakingOffDis() {
	return takingOffDis;
}
public void setTakingOffDis(int takingOffDis) {
	this.takingOffDis = takingOffDis;
}
public int getLandingDis() {
	return landingDis;
}
public void setLandingDis(int landingDis) {
	this.landingDis = landingDis;
}
   
}
