package com.demo.model;

public class Airport {
	private int id;
	private String state;
	private String identifier;
	private String city;
	private String name;
	private String size;
	private int enplanements;
	private int dailyUsage;
	private int scaled;
	private int landingFee;
	private int runwayLength;
	private int timeZone;
	private double longitude;
	private double latitude;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
	public String getIdentifier() {
		return identifier;
	}
	public void setIdentifier(String identifier) {
		this.identifier = identifier;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSize() {
		return size;
	}
	public void setSize(String size) {
		this.size = size;
	}
	public int getEnplanements() {
		return enplanements;
	}
	public void setEnplanements(int enplanements) {
		this.enplanements = enplanements;
	}
	public int getDailyUsage() {
		return dailyUsage;
	}
	public void setDailyUsage(int dailyUsage) {
		this.dailyUsage = dailyUsage;
	}
	public int getScaled() {
		return scaled;
	}
	public void setScaled(int scaled) {
		this.scaled = scaled;
	}
	public int getLandingFee() {
		return landingFee;
	}
	public void setLandingFee(int landingFee) {
		this.landingFee = landingFee;
	}
	public int getRunwayLength() {
		return runwayLength;
	}
	public void setRunwayLength(int runwayLength) {
		this.runwayLength = runwayLength;
	}
	public int getTimeZone() {
		return timeZone;
	}
	public void setTimeZone(int timeZone) {
		this.timeZone = timeZone;
	}
	public double getLongitude() {
		return longitude;
	}
	public void setLongitude(double longitude) {
		this.longitude = longitude;
	}
	public double getLatitude() {
		return latitude;
	}
	public void setLatitude(double latitude) {
		this.latitude = latitude;
	}

}
