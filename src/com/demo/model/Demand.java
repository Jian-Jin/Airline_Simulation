package com.demo.model;

public class Demand {
	private int timeZone;
	private int zulu;
	private int busRatio;
	private int econRatio;
	private int firstRatio;
	private int amount;
	private int cluster;
	public int getTimeZone() {
		return timeZone;
	}
	public void setTimeZone(int timeZone) {
		this.timeZone = timeZone;
	}
	public int getZulu() {
		return zulu;
	}
	public void setZulu(int zulu) {
		this.zulu = zulu;
	}
	public int getBusRatio() {
		return busRatio;
	}
	public void setBusRatio(int busRatio) {
		this.busRatio = busRatio;
	}
	public int getEconRatio() {
		return econRatio;
	}
	public void setEconRatio(int econRatio) {
		this.econRatio = econRatio;
	}
	public int getFirstRatio() {
		return firstRatio;
	}
	public void setFirstRatio(int firstRatio) {
		this.firstRatio = firstRatio;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
	public int getCluster() {
		return cluster;
	}
	public void setCluster(int cluster) {
		this.cluster = cluster;
	}
	
	
}
